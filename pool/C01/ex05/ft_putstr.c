#include <unistd.h>

void	ft_putstr(char	*str)
{
	int	n;
	int	i;

	i = 0;
	while (i < *str)
	{
		n = *str;
		if (n == 0)
		{
			return ;
		}
		write (1, &n, 1);
		str++;
	}
}
