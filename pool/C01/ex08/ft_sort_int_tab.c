void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	b;

	i = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				b = tab[i];
				tab[i] = tab[j];
				tab[j] = b;
			}
			j++;
		}
		i++;
	}
}
